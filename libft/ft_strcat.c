#include "libft.h"

char	*ft_strcat(char *dest, const char *src)
{
	int	len;
	int	i;

	if (!dest || !src)
		return (0);
	len = ft_strlen(dest);
	i = 0;
	while (src[i] != '\0')
	{
		dest[len + i] = src[i];
		i++;
	}
	dest[len + i] = '\0';
	return (dest);
}
